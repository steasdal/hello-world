package com.teasdale.commands;

/**
 * A simple command class that the PersonController will use to marshall incoming JSON
 */
public class PersonCommand {
    String name;
    int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
